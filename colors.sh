#!/usr/bin/env bash
# ============================================================
# colors.sh — shared logging helpers (ANSI colors)
# ============================================================
# Source this file from other scripts:
#   source "$(dirname "${BASH_SOURCE[0]}")/colors.sh"

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'
BLUE='\033[0;34m'
NC='\033[0m'

log_info() {
    echo -e "${BLUE}[INFO]${NC} $1"
}

log_success() {
    echo -e "${GREEN}[OK]${NC} $1"
}

log_warn() {
    echo -e "${YELLOW}[WARN]${NC} $1"
}

log_error() {
    echo -e "${RED}[ERROR]${NC} $1" >&2
}
